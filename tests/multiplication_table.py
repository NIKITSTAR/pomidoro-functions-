def multiplication_table():
    # Внешний цикл от 1 до 10 (каждая строка таблицы)
    for i in range(1, 11):
        # Внутренний цикл от 1 до 10 (каждый столбец в строке)
        for j in range(1, 11):
            # Вывод результата умножения i * j с небольшим выравниванием
            print(f"{i*j:4}", end="")
        # Переход на новую строку после каждого ряда
        print()

# Запускаем функцию
multiplication_table()


